@pytest.mark.parametrize("content,want_name,want_args", [
    # Plain `<function = "x">` with space around `=` and no `name` attr.
    # Captured from Qwen3.6-35B-A3B output in the 08:01 self-play trace.
    (
        '<tool_call>\n<function = "file_system">\n<parameter = "operation">read</parameter>\n<parameter = "path">sales.csv</parameter>\n</function>\n</tool_call>',
        "file_system",
        {"operation": "read", "path": "sales.csv"},
    ),
    # `<function_name="x">` hallucination — underscore alias.
    (
        '<tool_call>\n<function_name="execute">\n<parameter name="filename">sort.py</parameter>\n</function>\n</tool_call>',
        "execute",
        {"filename": "sort.py"},
    ),
    # `<function_name = "x">` — underscore alias with padded equals.
    (
        '<tool_call>\n<function_name = "file_system">\n<parameter name = "operation">write</parameter>\n<parameter name = "path">solution.py</parameter>\n<parameter name = "content">print(1)</parameter>\n</function>\n</tool_call>',
        "file_system",
        {"operation": "write", "path": "solution.py", "content": "print(1)"},
    ),
    # Mixed: dropped `name` on function, padded equals on parameters.
    (
        '<tool_call>\n<function = "execute">\n<parameter name = "command">head -5 sales.csv</parameter>\n</function>\n</tool_call>',
        "execute",
        {"command": "head -5 sales.csv"},
    ),
])
@pytest.mark.asyncio
async def test_qwen36_sloppy_xml_attributes_parse(agent, content, want_name, want_args):
    """Regression: Qwen3.6 emits XML with padded `=` and `function_name`
    aliases that the original parser regex `<function(?:\\s+name=|=)` did
    not match, causing every tool call to fall through to
    `system_parse_error`. The pre-parser normalization rewrites these
    variants into canonical `<function name="...">` form so the existing
    regex chain matches.
    """
    agent.context.llm_client.chat_completion = AsyncMock(side_effect=[
        {"choices": [{"message": {"content": content}}]},
        {"choices": [{"message": {"content": "done."}}]},
    ])
    background_tasks = MagicMock()
    body = {"messages": [{"role": "user", "content": "run it"}]}
    with patch(
        "ghost_agent.core.agent.get_active_tool_definitions",
        return_value=[{"function": {"name": want_name}}],
    ):
        await agent.handle_chat(body, background_tasks)
    tool_calls = body["messages"][2].get("tool_calls", [])
    assert len(tool_calls) == 1, f"expected 1 call, got {tool_calls}"
    assert tool_calls[0]["function"]["name"] == want_name
    got_args = json.loads(tool_calls[0]["function"]["arguments"])
    for k, v in want_args.items():
        assert got_args.get(k) == v, f"param {k!r}: want {v!r}, got {got_args.get(k)!r}"


