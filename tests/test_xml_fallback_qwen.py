@pytest.mark.asyncio
async def test_system_parse_error_on_mangled_xml_and_json(agent):
    """Test that if neither <function name="..."> nor valid JSON is found within a <tool_call>, it emits system_parse_error."""
    message = """Here is the broken call:
<tool_call>
<func_tion=broken>
Some random text { missing brace and stuff
</tool_call>"""

    agent.context.llm_client.chat_completion = AsyncMock(side_effect=[
        {"choices": [{"message": {"content": message}}]},
        {"choices": [{"message": {"content": "I fixed it", "tool_calls": []}}]}
    ])
    
    background_tasks = MagicMock()
    body = {"messages": [{"role": "user", "content": "please run the tool"}]}
    
    with patch("ghost_agent.core.agent.get_active_tool_definitions", return_value=[]):
        await agent.handle_chat(body, background_tasks)
        
    # The agent should have injected a system_parse_error feedback
    # Let's inspect the payload sent in the second call
    assert agent.context.llm_client.chat_completion.call_count >= 2
    second_call_args = agent.context.llm_client.chat_completion.call_args_list[1][0][0]
    sent_messages = second_call_args["messages"]
    
    error_tool_msg = next((m for m in sent_messages if "SYSTEM ERROR" in str(m.get("content"))), None)
    assert error_tool_msg is not None, "Agent did not inject system_parse_error feedback for invalid XML/JSON."
    # Recovery hint is now reason-specific. For the mangled `<func_tion>`
    # fixture here (no valid <function> tag), the branch is
    # "no_function_tag" which must still surface the correct shape.
    content = error_tool_msg["content"]
    assert "SYSTEM ERROR" in content
    assert (
        "no `<function" in content
        or "did not parse" in content
        or "CUT OFF" in content
    )


